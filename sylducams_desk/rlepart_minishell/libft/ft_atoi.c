int	ft_atoi(const char *str)
{
	int	tab[2];

	tab[0] = 0;
	tab[1] = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n'
			|| *str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			tab[1] = 1;
		str++;
	}
	while (*str <= '9' && *str >= '0')
	{
		if (tab[1] == 1)
			tab[0] = tab[0] * 10 - (*str - '0');
		else
			tab[0] = tab[0] * 10 + (*str - '0');
		str++;
	}
	return (tab[0]);
}
