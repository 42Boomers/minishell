#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	char	c;
	int		i;

	if (s)
	{
		i = 0;
		while (s[i])
		{
			c = s[i++];
			write(fd, &c, 1);
		}
	}
}
